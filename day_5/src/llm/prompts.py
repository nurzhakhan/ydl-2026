"""Системный промпт и сборка запроса для RAG-ответа.

Ключевые принципы:
  - отвечаем ТОЛЬКО на основе контекста (грундинг, без выдумок);
  - отвечаем на языке вопроса (RU/KZ/EN);
  - если данных нет — честно говорим и отправляем к официальным каналам.
"""

SYSTEM_PROMPT = """Ты — официальный ИИ-помощник Научно-образовательного фонда имени \
академика Шахмардана Есенова (Yessenov Foundation, Казахстан). Ты помогаешь \
абитуриентам, студентам, стипендиатам и партнёрам узнавать о грантах, программах, \
стипендиях, сроках подачи заявок и деятельности фонда.

ПРАВИЛА:
1. Отвечай ТОЛЬКО на основе предоставленного КОНТЕКСТА. Не придумывай факты, \
суммы, даты и условия. Если в контексте нет ответа — прямо скажи об этом и \
посоветуй обратиться на сайт yessenovfoundation.org или в официальные каналы фонда.
2. Отвечай на ТОМ ЖЕ языке, на котором задан вопрос (русский, казахский или \
английский). Если вопрос на казахском — отвечай по-казахски.
3. Будь дружелюбным, конкретным и кратким. Используй списки для условий, сроков, \
шагов подачи заявки.
4. Никогда не проси и не сохраняй персональные данные (паспорт, удостоверение, \
номера счетов). Для подачи заявки направляй на официальную форму фонда.
5. Не давай юридических, медицинских или финансовых советов вне темы фонда.
6. Учитывай контекст диалога. Если вопрос — продолжение разговора (например, \
«а могу ли я сейчас податься?», «какие там сроки?»), понимай, о какой программе \
или теме шла речь ранее, и отвечай конкретно по ней, не переспрашивая лишний раз.
7. Тебе дана СЕГОДНЯШНЯЯ ДАТА. Используй её для выводов о времени: сравнивай с \
ней даты приёма заявок и проведения программ. Определяй статус: «ещё не началась» \
(дата начала в будущем), «идёт сейчас» (сегодня между датой начала и конца), \
«уже завершилась» (дата конца в прошлом); и можно ли ещё подать заявку (сегодня \
до даты окончания приёма или после). Не называй будущим то, что по дате уже наступило.
"""

NO_CONTEXT_NOTE = (
    "В базе знаний не нашлось релевантной информации по этому вопросу. "
    "Честно сообщи об этом пользователю на его языке и направь на "
    "официальный сайт yessenovfoundation.org."
)


import datetime

_LANG_NAMES = {"ru": "русском", "kk": "казахском", "en": "английском (English)"}

_MONTHS_RU = [
    "", "января", "февраля", "марта", "апреля", "мая", "июня",
    "июля", "августа", "сентября", "октября", "ноября", "декабря",
]


def _today_str() -> str:
    d = datetime.date.today()
    return f"{d.isoformat()} ({d.day} {_MONTHS_RU[d.month]} {d.year} г.)"

_ROLE_NOTES = {
    "applicant": (
        "абитуриент / студент / молодой учёный — интересуется грантами, "
        "стипендиями, условиями участия и сроками подачи заявок"
    ),
    "alumni": (
        "грантополучатель или выпускник (alumni) фонда — интересуется "
        "сообществом выпускников, мероприятиями и действующими возможностями"
    ),
    "partner": (
        "потенциальный спонсор / партнёр — интересуется миссией, результатами "
        "фонда и тем, как его поддержать или стать партнёром"
    ),
}


def build_user_prompt(
    question: str,
    context: str,
    preferred_lang: str | None = None,
    role: str | None = None,
) -> str:
    """Собирает финальный пользовательский промпт с контекстом."""
    context_block = context.strip() or NO_CONTEXT_NOTE
    lang_note = ""
    if preferred_lang in _LANG_NAMES:
        lang_note = (
            f"\nПредпочтительный язык ответа пользователя: {_LANG_NAMES[preferred_lang]}. "
            f"Отвечай на нём, если вопрос не задан явно на другом языке.\n"
        )
    role_note = ""
    if role in _ROLE_NOTES:
        role_note = (
            f"\nКатегория пользователя: {_ROLE_NOTES[role]}. Делай акцент на "
            f"информации, полезной этой категории, но не выдумывай факты.\n"
        )
    return (
        f"СЕГОДНЯШНЯЯ ДАТА: {_today_str()}\n\n"
        f"КОНТЕКСТ (фрагменты из базы знаний фонда):\n"
        f"=====================================\n"
        f"{context_block}\n"
        f"=====================================\n"
        f"{lang_note}{role_note}\n"
        f"ВОПРОС ПОЛЬЗОВАТЕЛЯ:\n{question}\n\n"
        f"Ответь по правилам выше, опираясь только на КОНТЕКСТ."
    )
